function FormattingNumericalAxis(chart1)
{
	chart1.getAxisY().setForceZero(false);
PopulateFinancialData_Close(chart1);
chart1.getLegendBox().setVisible(false);
var titles = chart1.getTitles();
var title = new cfx.TitleDockable();
title.setText("AAPL Yearly Stock Price Variation");
titles.add(title);
chart1.getAllSeries().setMarkerShape(cfx.MarkerShape.None);
// RELEVANT CODE
chart1.getAxisY().getLabelsFormat().setFormat(cfx. AxisFormat.Currency);
chart1.getAxisY().getLabelsFormat().setDecimals(2);
// END RELEVANT CODE
}
function LogarithmicScale(chart1)
{
	PopulateDDOSAttack_HitsHour_HitsSec(chart1);
chart1.getAllSeries().setMarkerShape(cfx.MarkerShape.None);
var legendBox = chart1.getLegendBox();
legendBox.setDock(cfx.DockArea.Bottom);
legendBox.setContentLayout(cfx.ContentLayout.Center);
var titles = chart1.getTitles();
var title = new cfx.TitleDockable();
title.setText("Monthly DDOS Attack per hour and seconds");
titles.add(title);
// RELEVANT CODE
chart1.getAxisY().setLogBase(10);
// END RELEVANT CODE
}

